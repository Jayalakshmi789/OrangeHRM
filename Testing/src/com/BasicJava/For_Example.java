package com.BasicJava;

public class For_Example {

	public static void main(String[] args)
	
	{
		String var="Hello";
		
		for(int x=1;x<3;x++)
		{
			System.out.println(x +" "+ var);
		}
		
		System.out.println("*********************************");
for(int x=0;x>=-2;x--)
{
	System.out.println(x + " "+ var);
}
	}

}

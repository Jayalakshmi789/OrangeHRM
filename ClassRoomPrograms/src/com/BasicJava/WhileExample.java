package com.BasicJava;

public class WhileExample {

	public static void main(String[] args) {
		
		int a=2;
		
		while(a<=6)
		{
			System.out.println(a+" "+" Testing ");
			//a=a+3;
			a++;
		}
		
		System.out.println(" ******************** ");
		
		int b=-1;
		
		while(b<=4)
		{
			System.out.println(b+" - "+" LiveTech ");
			b++;
		}
		
		System.out.println(" ########################### ");
		
		int x=-6;
		
		while(x<=-6)
		{
			System.out.println(x+" "+" Selenium ");
			x++;
		}
	}
}

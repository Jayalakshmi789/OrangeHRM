package com.BasicJava;

public class Variable_Example {
	
	public static void main(String [] args)
	{
		// Variable 
		
		// Syntax:-  dataType variable = value;
		
		// Variable should be assigned with a value
		// Variable should be declared with a dataType
		// Variable is capable of storing only ONE value at a Time
		// Variable is capable of storing similar dataType values
	// Variable one declared with a dataType need not be declared again - if used again to
		//  store similar type of value
		
		int x=15;
		int y=10;
			
		/* System.out.print("the value of x is :");
		System.out.println(x); */
		
		System.out.println(" The value of variable x is : " + x);
		System.out.println(" The value of variable y is : " + y);
		
		char var ='a';
		System.out.println(" The value of variable var  is : "+var);
		
		float var1=5;
		System.out.println(" The value of varible var1  is : "+var1);
		
		double var2=12.345;
		System.out.println(" The value of varible var2  is : "+var2);
		
		String var3="LiveTech - Is a Testing Institute";
		System.out.println(" The value of varible var3  is : "+var3);
		
	}
}
